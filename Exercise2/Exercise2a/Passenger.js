class Passenger {
    constructor(name, destination) {
        this.name = name;
        this.destination = destination;
    }
    getName() { 
        return this.name;
    }
    setName(name) {
        this.name = name;
    }
    getDestination() {
        return this.destination;
    }
    setDestination(destination) {
        this.destination = destination;
    }
}